export default function EmojiPicker({ isOpen, onClose }) {
  const [copiedEmoji, setCopiedEmoji] = useState(null)

  const handleCopy = async (emoji) => {
    try {
      await navigator.clipboard.writeText(emoji)
      setCopiedEmoji(emoji)
      setTimeout(() => setCopiedEmoji(null), 1000)
    } catch {
      // Fallback
      const textArea = document.createElement('textarea')
      textArea.value = emoji
      document.body.appendChild(textArea)
      textArea.select()
      document.execCommand('copy')
      document.body.removeChild(textArea)
      setCopiedEmoji(emoji)
      setTimeout(() => setCopiedEmoji(null), 1000)
    }
  }

  if (!isOpen) return null

  return (
    <div className="emoji-overlay" onClick={onClose}>
      <div className="emoji-picker" onClick={(e) => e.stopPropagation()}>
        <div className="emoji-picker-header">
          <span>😊 常用 Emoji</span>
          <button className="emoji-close-btn" onClick={onClose}>✕</button>
        </div>
        <div className="emoji-picker-body">
          {EMOJI_CATEGORIES.map((cat) => (
            <div key={cat.name} className="emoji-category">
              <div className="emoji-category-name">{cat.name}</div>
              <div className="emoji-grid">
                {cat.emojis.map((emoji) => (
                  <button
                    key={emoji}
                    className={`emoji-item ${copiedEmoji === emoji ? 'copied' : ''}`}
                    onClick={() => handleCopy(emoji)}
                    title={`点击复制 ${emoji}`}
                  >
                    {emoji}
                    {copiedEmoji === emoji && <span className="emoji-copied-tip">已复制</span>}
                  </button>
                ))}
              </div>
            </div>
          ))}
        </div>
      </div>
    </div>
  )
}
